# Hashmap(Dictionary) :- Hashmap is a collection of key and value pair

# key never be duplicate but value same 2 key or more key

dict = {1:"India",2:"England",3:"Aus"}
# dict[key] = value (update the value)
dict[4] = "Pakistan"

for v in dict.values():
    print(v)

for k in dict.keys():
    print(k)

for k,v in dict.items():
    print(k,v)

